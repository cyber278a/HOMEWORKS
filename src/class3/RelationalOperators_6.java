package class3;

public class RelationalOperators_6 {
    public static void main(String[] args) {
/* relational operators
        >
        >=
        ==
        !=
        <
        <=
         */
        int number=10;
        int number2=20;
        System.out.println(number+number2);//arithmetic operators give results in same data type int+int=int

       boolean result= 20>10;
        System.out.println(result);// relational operators always give answer true or false
        System.out.println(20>10);//is 20 greater than 10
        System.out.println(5>10);
        System.out.println(5!=10);//is 5 not equal to 10
        System.out.println(5==10);// is 5 equal to 10
        System.out.println(5>=10);// is 5  greater or equal to 10
    }
}
