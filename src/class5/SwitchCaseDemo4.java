package class5;

public class SwitchCaseDemo4 {
    public static void main(String[] args) {
        String day="Friday";
        switch (day){
            case "Monday":
                System.out.println("1");
                break;
            case "Tuesday":
                System.out.println("2");
                break;
            default:
                System.out.println("Wrong day");
        }

    }
}
